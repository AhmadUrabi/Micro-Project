ORG 0000H

C1PORTA EQU 20H;
C1PORTB EQU 21H;
C1PORTC EQU 22H;
C2PORTA EQU 23H;
C2PORTB EQU 24H;
C2PORTC EQU 25H;

PROC PRINTCHAR
MOV AL, CL;
OUT C1PORTA, AL;
ENDP PRINTCHAR


; CLEAR SCREEN
MOV AL, 0;
OUT C1PORTB, AL;

XOR AX, AX;
XOR BX, BX;
MOV BX, FEH;
READLOOP: 
OUT C2PORTB, BL;
IN AL, C2PORTA;
TEST AL, FFH;
JZ READLOOP;
;; READ CURRENT BL AND AL TO SELECT CHARACTER AND STORE IN CL
CALL PRINTCHAR;
JMP READLOOP;

